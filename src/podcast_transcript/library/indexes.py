"""Regenerate the markdown indexes from ``episodes.jsonl``.

Five generated files all live under ``podcast-library/index/``:

- ``by-speaker.md`` — grouped by canonical speaker, then chronological.
- ``by-topic.md`` — grouped by canonical topic; an episode appears under
  each of its topics.
- ``by-date.md`` — reverse-chronological flat list.
- ``by-podcast.md`` — grouped by show; episodes with a ``series`` set are
  sub-grouped by series and sorted by ``series_part``.
- ``pending-vocab.md`` — lists topics/speakers awaiting vocab review,
  with the episodes that proposed them.

Every index file gets a one-line "generated by" header so a reader (or a
future Claude session) knows it's regenerated, not hand-edited.

The functions in this module are pure: they take a list of episodes and
return a string. :func:`rebuild_all` is the orchestrator that loads
``episodes.jsonl``, calls each builder, and writes the five files.
"""

from __future__ import annotations

from collections import defaultdict
from datetime import UTC, datetime
from typing import TYPE_CHECKING

from .store import load_all

if TYPE_CHECKING:
    from collections.abc import Iterable
    from pathlib import Path

    from .episode import Episode

__all__ = [
    "build_by_date",
    "build_by_podcast",
    "build_by_speaker",
    "build_by_topic",
    "build_pending_vocab",
    "rebuild_all",
]


def _header(title: str) -> str:
    stamp = datetime.now(UTC).isoformat(timespec="seconds")
    return (
        f"# {title}\n\n"
        f"_Generated by `podcast_transcript.library.indexes` at {stamp}._\n"
        f"_Edit `episodes.jsonl` and re-run `rebuild_indexes.py` — do not "
        f"hand-edit this file._\n\n"
    )


def _summary_link(episode: Episode) -> str:
    """Markdown link to the summary file, relative to ``index/``.

    Summary paths in the JSONL are stored relative to the library root, so
    going up one level (``..``) reaches that root from inside ``index/``.
    """
    return f"[{episode.episode_title}](../{episode.summary.path})"


def _qc_badge(episode: Episode) -> str:
    status = episode.summary.qc_status
    if status == "passed":
        return ""
    return f" _(QC: {status})_"


def build_by_date(episodes: Iterable[Episode]) -> str:
    """Reverse-chronological flat list."""
    ordered = sorted(episodes, key=lambda e: (e.pub_date, e.id), reverse=True)
    lines = [_header("Episodes by date")]
    if not ordered:
        lines.append("_No episodes yet._\n")
        return "".join(lines)
    for episode in ordered:
        speakers = ", ".join(episode.speakers) if episode.speakers else "—"
        lines.append(
            f"- **{episode.pub_date}** — {_summary_link(episode)} "
            f"_({episode.podcast})_ — {speakers}{_qc_badge(episode)}\n",
        )
    return "".join(lines)


def build_by_podcast(episodes: Iterable[Episode]) -> str:
    """Grouped by show, then by series (if any), then chronological."""
    by_show: dict[str, list[Episode]] = defaultdict(list)
    for ep in episodes:
        by_show[ep.podcast].append(ep)

    lines = [_header("Episodes by podcast")]
    if not by_show:
        lines.append("_No episodes yet._\n")
        return "".join(lines)

    for podcast in sorted(by_show):
        eps = by_show[podcast]
        lines.append(f"\n## {podcast}\n\n")
        with_series, standalone = _split_by_series(eps)
        for series_name in sorted(with_series):
            series_eps = sorted(
                with_series[series_name],
                key=lambda e: (e.series_part if e.series_part is not None else 0, e.pub_date),
            )
            lines.append(f"### {series_name}\n\n")
            for ep in series_eps:
                part = f"Part {ep.series_part}: " if ep.series_part is not None else ""
                lines.append(
                    f"- **{ep.pub_date}** — {part}{_summary_link(ep)}{_qc_badge(ep)}\n",
                )
            lines.append("\n")
        if standalone:
            if with_series:
                lines.append("### (standalone episodes)\n\n")
            for ep in sorted(standalone, key=lambda e: e.pub_date):
                lines.append(
                    f"- **{ep.pub_date}** — {_summary_link(ep)}{_qc_badge(ep)}\n",
                )
    return "".join(lines)


def _split_by_series(
    eps: list[Episode],
) -> tuple[dict[str, list[Episode]], list[Episode]]:
    with_series: dict[str, list[Episode]] = defaultdict(list)
    standalone: list[Episode] = []
    for ep in eps:
        if ep.series is not None:
            with_series[ep.series].append(ep)
        else:
            standalone.append(ep)
    return with_series, standalone


def build_by_speaker(episodes: Iterable[Episode]) -> str:
    """Grouped by speaker, then chronological."""
    by_speaker: dict[str, list[Episode]] = defaultdict(list)
    for ep in episodes:
        for speaker in ep.speakers:
            by_speaker[speaker].append(ep)

    lines = [_header("Episodes by speaker")]
    if not by_speaker:
        lines.append("_No speakers recorded yet._\n")
        return "".join(lines)

    for speaker in sorted(by_speaker):
        lines.append(f"\n## {speaker}\n\n")
        ordered = sorted(by_speaker[speaker], key=lambda e: e.pub_date)
        for ep in ordered:
            lines.append(
                f"- **{ep.pub_date}** — {_summary_link(ep)} _({ep.podcast})_{_qc_badge(ep)}\n",
            )
    return "".join(lines)


def build_by_topic(episodes: Iterable[Episode]) -> str:
    """Grouped by topic — episodes appear under every topic they cite."""
    by_topic: dict[str, list[Episode]] = defaultdict(list)
    for ep in episodes:
        for topic in ep.topics:
            by_topic[topic].append(ep)

    lines = [_header("Episodes by topic")]
    if not by_topic:
        lines.append("_No topics recorded yet._\n")
        return "".join(lines)

    for topic in sorted(by_topic):
        lines.append(f"\n## {topic}\n\n")
        ordered = sorted(by_topic[topic], key=lambda e: e.pub_date, reverse=True)
        for ep in ordered:
            lines.append(
                f"- **{ep.pub_date}** — {_summary_link(ep)} _({ep.podcast})_{_qc_badge(ep)}\n",
            )
    return "".join(lines)


def build_pending_vocab(episodes: Iterable[Episode]) -> str:
    """Surface unknown topics/speakers awaiting review.

    Aggregates by name across episodes so the reader can see, for
    instance, every episode that proposed a given pending topic. The
    expected workflow is: read this file, decide whether each entry
    should be promoted to ``canonical`` or aliased to an existing one,
    edit the vocab JSON, then re-run ``rebuild_indexes``.
    """
    pending_topics: dict[str, list[Episode]] = defaultdict(list)
    pending_speakers: dict[str, list[Episode]] = defaultdict(list)
    for ep in episodes:
        for t in ep.pending_topics:
            pending_topics[t].append(ep)
        for s in ep.pending_speakers:
            pending_speakers[s].append(ep)

    lines = [_header("Pending vocabulary")]
    lines.append(
        "These topics / speakers were proposed by the summariser but are not "
        "yet in `vocab/topics.json` or `vocab/speakers.json`. Promote each "
        "entry to `canonical` or add an `aliases` mapping, then re-run "
        "`rebuild_indexes.py` to re-normalise the JSONL.\n\n",
    )

    lines.append("## Topics\n\n")
    if not pending_topics:
        lines.append("_None pending._\n\n")
    else:
        for topic in sorted(pending_topics):
            ep_list = ", ".join(
                f"{_summary_link(ep)} ({ep.pub_date})"
                for ep in sorted(pending_topics[topic], key=lambda e: e.pub_date)
            )
            lines.append(f"- **{topic}** — {ep_list}\n")
        lines.append("\n")

    lines.append("## Speakers\n\n")
    if not pending_speakers:
        lines.append("_None pending._\n")
    else:
        for speaker in sorted(pending_speakers):
            ep_list = ", ".join(
                f"{_summary_link(ep)} ({ep.pub_date})"
                for ep in sorted(pending_speakers[speaker], key=lambda e: e.pub_date)
            )
            lines.append(f"- **{speaker}** — {ep_list}\n")

    return "".join(lines)


def rebuild_all(*, index_dir: Path, jsonl_path: Path | None = None) -> dict[str, Path]:
    """Regenerate all five markdown indexes from *jsonl_path*.

    Returns a mapping of file label → path written. Idempotent: re-running
    on an unchanged JSONL produces byte-identical output apart from the
    timestamp in the header.
    """
    if jsonl_path is None:
        jsonl_path = index_dir / "episodes.jsonl"
    episodes = load_all(jsonl_path)

    outputs: dict[str, str] = {
        "by-speaker.md": build_by_speaker(episodes),
        "by-topic.md": build_by_topic(episodes),
        "by-date.md": build_by_date(episodes),
        "by-podcast.md": build_by_podcast(episodes),
        "pending-vocab.md": build_pending_vocab(episodes),
    }
    index_dir.mkdir(parents=True, exist_ok=True)
    written: dict[str, Path] = {}
    for name, body in outputs.items():
        path = index_dir / name
        path.write_text(body, encoding="utf-8")
        written[name] = path
    return written
